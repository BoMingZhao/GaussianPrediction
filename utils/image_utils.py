import torch
import numpy as np

def mse(img1, img2):
    return (((img1 - img2)) ** 2).view(img1.shape[0], -1).mean(1, keepdim=True)

def psnr(img1, img2):
    mse = (((img1 - img2)) ** 2).view(img1.shape[0], -1).mean(1, keepdim=True)
    return 20 * torch.log10(1.0 / torch.sqrt(mse))

def psnr_diff(img1, img2):
    # image size (B, 3, H, W)
    mse = (((img1 - img2)) ** 2).mean(1, keepdim=True).view([1, img1.shape[-2], img1.shape[-1]])
    return mse.cpu().numpy()
